/**
 * Migrate Coach Teams from coachteamsTable.json to Supabase
 * Run: node scripts/migrate-coach-teams-to-supabase.js
 */

const { createClient } = require('@supabase/supabase-js');
const fs = require('fs');
const path = require('path');

// Load environment variables
require('dotenv').config();

const supabaseUrl = process.env.SUPABASE_URL;
const supabaseKey = process.env.SUPABASE_ANON_KEY;

if (!supabaseUrl || !supabaseKey) {
  console.error('❌ Missing SUPABASE_URL or SUPABASE_ANON_KEY in .env file');
  process.exit(1);
}

const supabase = createClient(supabaseUrl, supabaseKey);

async function migrateCoachTeams() {
  console.log('🚀 Starting Coach Teams migration to Supabase...\n');

  // Read JSON file
  const jsonPath = path.join(__dirname, '..', '..', 'coachteamsTable.json');
  const rawData = fs.readFileSync(jsonPath, 'utf-8');
  const teamRecords = JSON.parse(rawData);

  console.log(`📊 Found ${teamRecords.length} coach team records to migrate\n`);

  // Transform data - exclude timestamps (auto-generated by Supabase)
  const transformedRecords = teamRecords.map(record => ({
    Id: record.Id,
    TeamId: record.TeamId,
    CoachId: record.CoachId,
    CoCoachId: record.CoCoachId,
    Status: record.Status
    // CreatedAt and UpdatedAt are auto-generated
  }));

  // Insert in batches of 50
  const batchSize = 50;
  let successCount = 0;
  let errorCount = 0;

  for (let i = 0; i < transformedRecords.length; i += batchSize) {
    const batch = transformedRecords.slice(i, i + batchSize);
    const batchNum = Math.floor(i / batchSize) + 1;
    const totalBatches = Math.ceil(transformedRecords.length / batchSize);

    console.log(`📦 Processing batch ${batchNum}/${totalBatches} (${batch.length} records)...`);

    const { data, error } = await supabase
      .from('coach_teams_table')
      .upsert(batch, { onConflict: 'Id' });

    if (error) {
      console.error(`❌ Error in batch ${batchNum}:`, error.message);
      console.error('   Details:', error.hint || error.details || 'No additional details');
      errorCount += batch.length;
    } else {
      console.log(`✅ Batch ${batchNum} inserted successfully`);
      successCount += batch.length;
    }
  }

  console.log('\n' + '='.repeat(50));
  console.log('📊 Migration Summary:');
  console.log('='.repeat(50));
  console.log(`✅ Successfully migrated: ${successCount} records`);
  console.log(`❌ Failed: ${errorCount} records`);
  console.log(`📈 Total processed: ${transformedRecords.length} records`);
  console.log('='.repeat(50));

  if (successCount === transformedRecords.length) {
    console.log('\n🎉 All coach team records migrated successfully!');
  } else {
    console.log('\n⚠️  Some records failed to migrate. Check errors above.');
  }
}

// Run migration
migrateCoachTeams()
  .then(() => {
    console.log('\n✨ Migration completed');
    process.exit(0);
  })
  .catch(error => {
    console.error('\n❌ Migration failed:', error);
    process.exit(1);
  });
