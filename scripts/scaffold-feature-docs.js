#!/usr/bin/env node
/**
 * One-shot scaffolder: drops a README.md and __tests__/MATRIX.md stub into
 * every backend/frontend feature folder that doesn't already have them.
 *
 * Idempotent — never overwrites existing files. Safe to re-run.
 * Run via: node scripts/scaffold-feature-docs.js
 */
const fs = require('fs');
const path = require('path');

const repoRoot = path.resolve(__dirname, '..');

const targets = [
  { root: path.join(repoRoot, 'backend', 'features'), side: 'backend' },
  { root: path.join(repoRoot, 'frontend', 'src', 'features'), side: 'frontend' },
];

function readmeTemplate(name, side) {
  const layerHint = side === 'backend'
    ? '`api/`, `domain/`, `validation/`, `data/`, `__tests__/`'
    : '`components/`, `hooks/`, `api/`, `domain/`, `__tests__/`';
  return `# Feature: ${name} (${side})

> Stub generated by \`scripts/scaffold-feature-docs.js\`. Fill in each section
> before this feature next ships. Reference: [claude.md §2](../../../claude.md#2-architecture-governance).

## Purpose
TODO — one paragraph on the user-facing capability this feature provides.

## Owners
- CODEOWNER: see [\`.github/CODEOWNERS\`](../../../.github/CODEOWNERS)
- Domain expert: TODO
- On-call: TODO

## Public API
TODO — list the public entrypoints (HTTP routes, exported hooks, exported
components). Anything not listed here is considered internal and may change
without notice.

## Internal layout
This feature follows Vertical Slice Architecture. Allowed sub-folders:
${layerHint}.

## Dependencies
- Internal: TODO (shared helpers, other feature events)
- External: TODO (Supabase tables, external APIs, env vars)

## Threat model (claude.md §8.5)
- Auth: TODO
- Authorization: TODO (which roles / RLS policies)
- PII handled: TODO
- Rate limits: TODO

## Feature flags
- TODO (\`ff.${name}.*\`)

## Related ADRs
- TODO

## Tests
See [\`__tests__/MATRIX.md\`](./__tests__/MATRIX.md).
`;
}

function matrixTemplate(name) {
  return `# Test Matrix — ${name}

Reference: [claude.md §9.3](../../../../claude.md#93-feature-testing-matrix-template).

| Capability | Unit | Integration | E2E | Permissions | Edge cases |
|------------|------|-------------|-----|-------------|------------|
| _TODO_     |  ⬜  |     ⬜      | ⬜  |     ⬜      |   ⬜       |

Legend: ✅ covered · ⚠️ partial · ❌ missing · ⬜ not yet assessed.

## User journeys (claude.md §9.4)
List the 3-5 human-readable journeys this feature must support and link the
e2e tests that cover them.

1. TODO
2. TODO

## Known gaps
List any capability that is intentionally NOT tested yet and why, with a
target sprint to close the gap.

- TODO
`;
}

let created = 0;
let skipped = 0;
const log = [];

for (const { root, side } of targets) {
  if (!fs.existsSync(root)) continue;
  const features = fs
    .readdirSync(root, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name);

  for (const name of features) {
    const readmePath = path.join(root, name, 'README.md');
    const testsDir = path.join(root, name, '__tests__');
    const matrixPath = path.join(testsDir, 'MATRIX.md');

    if (!fs.existsSync(readmePath)) {
      fs.writeFileSync(readmePath, readmeTemplate(name, side), 'utf8');
      created++;
      log.push(`+ ${path.relative(repoRoot, readmePath)}`);
    } else {
      skipped++;
    }

    if (!fs.existsSync(testsDir)) fs.mkdirSync(testsDir, { recursive: true });
    if (!fs.existsSync(matrixPath)) {
      fs.writeFileSync(matrixPath, matrixTemplate(name), 'utf8');
      created++;
      log.push(`+ ${path.relative(repoRoot, matrixPath)}`);
    } else {
      skipped++;
    }
  }
}

console.log(log.join('\n'));
console.log(`\nDone. Created ${created} file(s), skipped ${skipped} existing.`);
